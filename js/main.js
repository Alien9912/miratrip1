function updateFact() {
    const factBlocks = document.querySelectorAll('.fact-block');
    if (factBlocks.length === 0) return;
    const randomIndex = Math.floor(Math.random() * facts.length);
    const newFact = facts[randomIndex];
    factBlocks.forEach(block => {
        block.style.opacity = '0';
        setTimeout(() => {
            const factText = block.querySelector('.fact-text');
            if (factText) {
                factText.textContent = newFact;
            } else {
                block.innerHTML = `<i class="fas fa-scroll"></i><p class="fact-text">${newFact}</p>`;
            }
            block.style.opacity = '1';
        }, 300);
    });
}

document.addEventListener('DOMContentLoaded', () => {
    updateFact();
    setInterval(updateFact, 30000);
});

